def add (a,b):
    answer = (a+b)
    return answer

# subtract
def subtract (z,x):
    answer = (z-x)
    return answer


# divide
def divide (v,n):
    answer = (v/n)
    return answer


# multiply
def multiply (k,m):
    answer = (k*m)
    return answer


# remainder
def remainder (p,t):
    answer = (p%t)
    return answer